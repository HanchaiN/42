#include<unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(STDOUT_FILENO, str, 1);
		str++;
	}
	return ;
}

int	main(int argc, char **argv)
{
	int	i;

	i = argc - 1;
	while (i > 0)
	{
		ft_putstr(argv[i]);
		write(STDOUT_FILENO, "\n", 1);
		i--;
	}
	return (0);
}
