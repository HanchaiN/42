char	*ft_strlowcase(char *str)
{
	char	*str_;

	str_ = str;
	while (*str != '\0')
	{
		if ('A' <= *str && *str <= 'Z')
		{
			*str += 'a' - 'A';
		}
		str++;
	}
	return (str_);
}
