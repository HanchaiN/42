char	*ft_strupcase(char *str)
{
	char	*str_;

	str_ = str;
	while (*str != '\0')
	{
		if ('a' <= *str && *str <= 'z')
		{
			*str += 'A' - 'a';
		}
		str++;
	}
	return (str_);
}
